package com.code;
public class Code {
	
	public int Suma(int a, int b) {
		int c = a + b;
		return c;
	}
	
	public int Resta(int a, int b) {
		int c = a - b;
		return c;
	}
	
	public int Multiplicacion(int a, int b) {
		int c = a * b;
		return c;
	}
	
	public int Division(int a, int b) {
		int c = a / b;
		return c;
	}
	
	public int contador(int x) {
		int T = x + 1;
		return T;
	}

}
